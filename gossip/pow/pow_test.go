type TM struct {
	header uint16
	data   []byte
	nonce  uint64
}

func (x *TM) Marshal(buf []byte) ([]byte, error) {
	buf = slices.Grow(buf, binary.Size(x.header)+len(x.data)+binary.Size(x.nonce))
	buf = buf[:binary.Size(x.header)+len(x.data)+binary.Size(x.nonce)]

	idx := 0

	binary.BigEndian.PutUint16(buf[idx:], x.header)
	idx += binary.Size(x.header)

	copy(buf[idx:], x.data[:])
	idx += len(x.data)

	binary.BigEndian.PutUint64(buf[idx:], x.nonce)
	idx += binary.Size(x.nonce)

	return buf, nil
}

func (x *TM) Nonce() uint64 {
	return x.nonce
}

func (x *TM) SetNonce(n uint64) {
	x.nonce = n
}

func (x *TM) AddToNonce(n uint64) {
	x.nonce += n
}

func (x *TM) StripPrefixLen() uint {
	return uint(binary.Size(x.header))
}

func (x *TM) PrefixLen() uint {
	return uint(binary.Size(x.data))
}

func (x *TM) WriteNonce(w io.Writer) {
	w.Write(binary.BigEndian.AppendUint64(nil, x.nonce))
}

func (x *TM) Clone() POWMarshaller[uint64] {
	return &TM{
		header: x.header,
		data:   x.data,
		nonce:  x.nonce,
	}
}
